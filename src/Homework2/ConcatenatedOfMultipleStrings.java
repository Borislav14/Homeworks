package Homework2;

public class ConcatenatedOfMultipleStrings {
    public static void main(String[] args) {
        String str1 = "My";
        String str2 = "name";
        String str3 = "is";
        String str4 = "Borislav";

        String combinedString = str1 + " " + str2 + " " + str3 + " " + str4;
        System.out.println("concatenated string: " + combinedString);
    }
}